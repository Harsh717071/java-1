package corejava;
//JavA program to calculate sum of first 10 natural number.

public class NaturalNumber {
// Main driver method
	public static void main(String[] args) 
	{
	// Creating integer and sum variable
	int i,num =10,sum = 0;
	// execute the condition 
		
		for(i=1;i<=num;i++) 
		{
		sum=sum+i;
		}
		//printing sum variable
		System.out.println("Sum of First 10 Natural Number is="+ sum);

	}

}
