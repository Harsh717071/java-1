package corejava;
// Java program to print following pattern 
 class StarPatternAss 
 {
// Main driver method
	 public static void main(String[]args) {
		 // Creating integer value
		 int i ,j;
		// executes the condition 
	    for(i=0 ; i<=5; i++ ) 
	    {
	    	
	    	for(j=0 ; j<i; j++) 
	    	{
	        	System.out.print("*");
	        	
	        }
	    	// print the star pattern
	    	System.out.println("");
	    }
		 
	 }
}




