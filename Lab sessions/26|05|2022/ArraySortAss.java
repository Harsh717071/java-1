 package corejava;
// Java Program to Sort an Array an decreasing order.
import java.util.Arrays;
// Main Class
public class ArraySortAss
{
// Main method
	public static void main(String[] args) 
	{
		// Input for Array
	int [] arr = {98,65,94,38,68,46,12};
	// Applying sort() method for above Array
	Arrays.sort (arr);
	// Printing arrays after sorting
	System.out.println("Modified arr[] : %s"+ Arrays.toString(arr));                     

	}
}


