package corejava;

public class explictconversion {

	public static void main(String[]args) {
		
		double d = 200.06;
		
		//explicit type casting 
		 
		long l = (long) d;
		
		//explicit type casting 
		
		int i = (int) l ;
		
		System.out.println("double value " + d);
		System.out.println("long value " + l);
		System.out.println("int value " + i);
	}
}
