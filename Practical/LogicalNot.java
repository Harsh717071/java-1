package corejava;

public class LogicalNot {
public static void main(String[]args) {
	//initializing variable
	int a= 10,b = 1;
	//Displaying a,b
	System.out.println("var 1 =" + a);
	System.out.println("var 2 =" + b );
	
	//using logical NOT operator
	System.out.println("!(a<b) = " + !(a<b));
	System.out.println("!(a>b) = " + !(a>b));

}
}
